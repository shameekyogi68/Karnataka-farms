        function addToCart(item) {
            const existing = cart.find(c => c.id === item.id && c.size === item.size && c.pot === item.pot);
            if (existing) {
                existing.qty += item.qty;
            } else {
                cart.push(item);
            }
            saveCart();
            updateCartUI();
            toggleCart();
            showToast(item.name + ' added to cart!', 'success');
        }

        function removeFromCart(index) {
            const items = document.querySelectorAll('.cart-item');
            if (items[index]) {
                items[index].classList.add('removing');
                setTimeout(() => {
                    cart.splice(index, 1);
                    saveCart();
                    updateCartUI();
                }, 400);
            }
        }

        function updateCartQty(index, change) {
            cart[index].qty += change;
            if (cart[index].qty < 1) {
                removeFromCart(index);
                return;
            }
            saveCart();
            updateCartUI();
        }

        function saveCart() {
            localStorage.setItem('karnatakaCart', JSON.stringify(cart));
        }

